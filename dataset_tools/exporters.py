"""Exporters: master -> masks, YOLO-seg, semantic, COCO.

Every exporter is driven by the same persisted split (``splits/``), so the three
model formats stay perfectly aligned. Images with no annotations flow through as
intentional negatives — an empty YOLO ``.txt``, an all-zero semantic mask, and a
COCO image entry with no annotations.
"""
from __future__ import annotations

import json
import shutil
from pathlib import Path
from typing import Iterable

import cv2
import numpy as np

from . import geometry
from .config import (
    COCO_CATEGORY_ID,
    MASK_FG,
    SMOOTHIE_NAME,
    SPLITS,
    YOLO_CLASS_ID,
    DatasetPaths,
)
from .master import Master
from .splits import read_splits


# ---- small utilities --------------------------------------------------------
def _progress(iterable: Iterable, desc: str):
    """tqdm if available, else a silent pass-through (keeps tqdm optional)."""
    try:
        from tqdm import tqdm  # type: ignore
        return tqdm(iterable, desc=desc)
    except Exception:
        return iterable


def _source_image(paths: DatasetPaths, file_name: str) -> Path | None:
    """Locate an image: master/images first, then raw_images."""
    for base in (paths.master_images, paths.raw_images):
        p = base / file_name
        if p.exists():
            return p
    return None


def _fresh_dir(path: Path) -> None:
    """Create ``path`` empty (remove any stale prior export)."""
    if path.exists():
        shutil.rmtree(path)
    path.mkdir(parents=True, exist_ok=True)


def _images_by_stem(master: Master) -> dict[str, dict]:
    return {master.image_stem(im): im for im in master.images}


# ---- 1. generate binary masks ----------------------------------------------
def generate_masks(master: Master, paths: DatasetPaths) -> int:
    """Rasterize each image's smoothie polygons into a combined 0/1 mask PNG.

    All annotations for an image are merged into ONE binary mask (semantic-seg
    convention). Negatives produce an all-zero mask. Returns the count written.
    """
    paths.master_masks.mkdir(parents=True, exist_ok=True)
    written = 0
    for im in _progress(master.images, "masks"):
        stem = master.image_stem(im)
        w, h = int(im["width"]), int(im["height"])
        polygons: list[list[float]] = []
        for ann in master.annotations_for(im["id"]):
            polygons.extend(ann["segmentation"])
        mask = geometry.polygons_to_mask(polygons, w, h, fg=MASK_FG)
        cv2.imwrite(str(paths.master_masks / f"{stem}.png"), mask)
        written += 1
    return written


def _load_or_make_mask(master: Master, paths: DatasetPaths, im: dict) -> np.ndarray:
    """Return the 0/1 mask for an image, preferring a cached master mask PNG."""
    stem = master.image_stem(im)
    cached = paths.master_masks / f"{stem}.png"
    if cached.exists():
        m = cv2.imread(str(cached), cv2.IMREAD_GRAYSCALE)
        if m is not None:
            return (m > 0).astype(np.uint8) * MASK_FG
    polygons: list[list[float]] = []
    for ann in master.annotations_for(im["id"]):
        polygons.extend(ann["segmentation"])
    return geometry.polygons_to_mask(polygons, int(im["width"]), int(im["height"]), fg=MASK_FG)


# ---- 2. YOLO-seg export -----------------------------------------------------
def export_yolo(master: Master, paths: DatasetPaths) -> dict[str, int]:
    """Write ``exports/yolo_seg/`` (images + polygon .txt labels + data.yaml)."""
    splits = read_splits(paths)
    by_stem = _images_by_stem(master)
    counts = {s: 0 for s in SPLITS}

    for split in SPLITS:
        img_out = paths.yolo_dir / "images" / split
        lbl_out = paths.yolo_dir / "labels" / split
        _fresh_dir(img_out)
        _fresh_dir(lbl_out)

        for stem in _progress(splits.get(split, []), f"yolo:{split}"):
            im = by_stem.get(stem)
            if im is None:
                continue
            src = _source_image(paths, im["file_name"])
            if src is None:
                print(f"  yolo: image missing on disk for '{stem}', skipping")
                continue
            shutil.copyfile(src, img_out / im["file_name"])

            objs = [ann["segmentation"] for ann in master.annotations_for(im["id"])]
            rows = geometry.to_yolo_rows(objs, int(im["width"]), int(im["height"]),
                                         class_id=YOLO_CLASS_ID)
            # Empty file for negatives — YOLO treats it as a background image.
            (lbl_out / f"{stem}.txt").write_text("\n".join(rows) + ("\n" if rows else ""))
            counts[split] += 1

    _write_data_yaml(paths)
    return counts


def _write_data_yaml(paths: DatasetPaths) -> None:
    paths.yolo_dir.mkdir(parents=True, exist_ok=True)
    (paths.yolo_dir / "data.yaml").write_text(
        f"# YOLO-seg dataset — generated by dataset_tools. Do not edit by hand.\n"
        f"path: {paths.yolo_dir.resolve()}\n"
        f"train: images/train\n"
        f"val: images/val\n"
        f"test: images/test\n"
        f"\n"
        f"nc: 1\n"
        f"names:\n"
        f"  {YOLO_CLASS_ID}: {SMOOTHIE_NAME}\n"
    )


# ---- 3. semantic-segmentation export ----------------------------------------
def export_semantic(master: Master, paths: DatasetPaths) -> dict[str, int]:
    """Write ``exports/semantic/`` — image + single-channel 0/1 mask pairs."""
    splits = read_splits(paths)
    by_stem = _images_by_stem(master)
    counts = {s: 0 for s in SPLITS}

    for split in SPLITS:
        img_out = paths.semantic_dir / "images" / split
        msk_out = paths.semantic_dir / "masks" / split
        _fresh_dir(img_out)
        _fresh_dir(msk_out)

        for stem in _progress(splits.get(split, []), f"semantic:{split}"):
            im = by_stem.get(stem)
            if im is None:
                continue
            src = _source_image(paths, im["file_name"])
            if src is None:
                print(f"  semantic: image missing on disk for '{stem}', skipping")
                continue
            shutil.copyfile(src, img_out / im["file_name"])
            mask = _load_or_make_mask(master, paths, im)
            # Mask stem matches the image stem so loaders can pair them 1:1.
            cv2.imwrite(str(msk_out / f"{stem}.png"), mask)
            counts[split] += 1

    return counts


# ---- 4. COCO export ---------------------------------------------------------
def export_coco(master: Master, paths: DatasetPaths) -> dict[str, int]:
    """Write ``exports/coco/`` — per-split instances_*.json + copied images."""
    splits = read_splits(paths)
    by_stem = _images_by_stem(master)
    counts = {s: 0 for s in SPLITS}
    (paths.coco_dir / "annotations").mkdir(parents=True, exist_ok=True)

    for split in SPLITS:
        img_out = paths.coco_dir / "images" / split
        _fresh_dir(img_out)

        images: list[dict] = []
        annotations: list[dict] = []
        for stem in _progress(splits.get(split, []), f"coco:{split}"):
            im = by_stem.get(stem)
            if im is None:
                continue
            src = _source_image(paths, im["file_name"])
            if src is None:
                print(f"  coco: image missing on disk for '{stem}', skipping")
                continue
            shutil.copyfile(src, img_out / im["file_name"])

            images.append({
                "id": im["id"],
                "file_name": im["file_name"],
                "width": int(im["width"]),
                "height": int(im["height"]),
            })
            for ann in master.annotations_for(im["id"]):
                annotations.append({
                    "id": ann["id"],
                    "image_id": im["id"],
                    "category_id": COCO_CATEGORY_ID,
                    "segmentation": ann["segmentation"],
                    "bbox": ann.get("bbox") or geometry.polygon_bbox(ann["segmentation"]),
                    "area": ann.get("area") or geometry.polygon_area(
                        ann["segmentation"], int(im["width"]), int(im["height"])),
                    "iscrowd": ann.get("iscrowd", 0),
                })
            counts[split] += 1

        doc = {
            "info": {"description": f"Smoothie COCO export ({split})", "version": "1.0"},
            "categories": [{"id": COCO_CATEGORY_ID, "name": SMOOTHIE_NAME,
                            "supercategory": "food"}],
            "images": images,
            "annotations": annotations,
        }
        out = paths.coco_dir / "annotations" / f"instances_{split}.json"
        out.write_text(json.dumps(doc, indent=2))

    return counts


def export_all(master: Master, paths: DatasetPaths) -> None:
    """Run mask generation + all three exports in the canonical order."""
    print("generating master masks...")
    n = generate_masks(master, paths)
    print(f"  {n} masks written")
    print("exporting YOLO-seg...")
    print(f"  {export_yolo(master, paths)}")
    print("exporting semantic...")
    print(f"  {export_semantic(master, paths)}")
    print("exporting COCO...")
    print(f"  {export_coco(master, paths)}")
    print("export-all complete.")
